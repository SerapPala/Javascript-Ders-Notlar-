/*

MANTIKSAL OPERATORLER

&& : ve
|| : veya
!  : değil

*/


// &&
let yas = 17
let cuzdan = 1000
if(yas>18 && cuzdan > 1000){
    console.log("Doğru");
}else {
    console.log("Yanlıs")
}
// || 
if(yas>18 || cuzdan >= 1000){
    console.log("Doğru");
}else {
    console.log("Yanlıs")
}
// !
if(yas>18 || !cuzdan >= 1000){
    console.log("Doğru");
}else {
    console.log("Yanlıs")
}
